export function TipJourneyClosingSection({ data }: { data: TipReportData }) {
  const currentRating = getRating(data.currentScore);
  const projectedRating = getRating(data.projectedScore);
  const steps = ["Assess", "Improve", "Maintain"] as const;
  const activePhase = data.journeyPhaseLabel.toLowerCase();

  return (
    <>
      <ReportPageBreak />
      <ReportSection title="Your Technology Journey" documentTheme={DOCUMENT_THEME}>
        <div className="tip-closing-hero">
          <h3 className="tip-closing-hero-title">Assess → Improve → Maintain</h3>
          <p className="tip-closing-hero-subtitle">
            {BRAND.companyName} partners with organizations through Assess → Improve → Maintain to
            deliver measurable, lasting technology outcomes.
          </p>
          <div className="tip-closing-steps">
            {steps.map((step) => (
              <div key={step} className="tip-closing-step">
                <div
                  className={cn(
                    "tip-closing-step-dot",
                    activePhase.startsWith(step.toLowerCase().slice(0, 4)) &&
                      "tip-closing-step-dot-active",
                  )}
                />
                <p className="tip-closing-step-label">{step}</p>
              </div>
            ))}
          </div>
        </div>

        <div className="tip-closing-scores">
          <TipMaturityScoreMini
            label="Starting StackScore"
            score={data.currentScore}
            ratingLabel={RATING_LABELS[currentRating]}
          />
          <TipMaturityScoreMini
            label="Target StackScore"
            score={data.projectedScore}
            ratingLabel={RATING_LABELS[projectedRating]}
            projected
          />
        </div>

        <p className="mt-4 text-center text-xs text-[color:var(--report-muted)]">
          Generated by {BRAND.productName} on {data.generatedDate} · {BRAND.companyName} ·{" "}
          {BRAND.website}
        </p>
      </ReportSection>
    </>
  );
}

