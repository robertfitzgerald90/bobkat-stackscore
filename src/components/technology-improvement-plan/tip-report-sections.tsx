type TipExecutiveSummarySectionProps = {
  data: TipReportData;
  executiveSummary: string;
  isEditable?: boolean;
  onExecutiveSummaryChange?: (value: string) => void;
};

export function TipExecutiveSummarySection({
  data,
  executiveSummary,
  isEditable,
  onExecutiveSummaryChange,
}: TipExecutiveSummarySectionProps) {
  const fallback = buildTipExecutiveFallback(
    data.clientName,
    data.currentScore,
    data.projectedScore,
  );
  const paragraphs = (executiveSummary || fallback).trim().split(/\n\n+/).filter(Boolean);

  return (
    <ReportSection
      title="Executive Summary"
      subtitle="Business context, profile trajectory, and expected outcomes"
      documentTheme={DOCUMENT_THEME}
    >
      <ReportPrintLeadGroup>
        {isEditable && onExecutiveSummaryChange ? (
          <ReportExecutiveSummary
            value={executiveSummary}
            isEditable
            onChange={onExecutiveSummaryChange}
            fallback={fallback}
            className="tip-executive-prose min-h-32 rounded-lg border bg-[color:var(--report-background)] px-3 py-2"
          />
        ) : (
          <div className="tip-executive-prose-stack tip-executive-prose">
            {paragraphs.map((paragraph) => (
              <p key={paragraph.slice(0, 48)}>{paragraph}</p>
            ))}
          </div>
        )}
        <TipMaturityHero
          currentScore={data.currentScore}
          projectedScore={data.projectedScore}
          scoreImprovement={data.scoreImprovement}
          journeyPhaseLabel={data.journeyPhaseLabel}
          journeyProgressPercent={data.journeyProgressPercent}
        />
      </ReportPrintLeadGroup>
    </ReportSection>
  );
}

